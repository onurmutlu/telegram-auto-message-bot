    def save_config(self, config_path: str = "config/config.json") -> bool:
        """
        YapÄ±landÄ±rma ayarlarÄ±nÄ± JSON dosyasÄ±na kaydeder
        Args:
            config_path: KayÄ±t yapÄ±lacak dosya yolu
        Returns:
            bool: BaÅŸarÄ±lÄ± ise True
        """
        try:
            # Dizini oluÅŸtur (yoksa)
            Path(config_path).parent.mkdir(parents=True, exist_ok=True)
            
            # Config nesnesini sÃ¶zlÃ¼ÄŸe dÃ¶nÃ¼ÅŸtÃ¼r
            config_dict = {}
            for key, default_value in self.DEFAULT_CONFIG.items():
                value = getattr(self, key)
                # Path nesnelerini string'e dÃ¶nÃ¼ÅŸtÃ¼r
                if isinstance(value, Path):
                    value = str(value)
                config_dict[key] = value
            
            # JSON olarak kaydet
            with open(config_path, 'w', encoding='utf-8') as f:
                json.dump(config_dict, f, indent=4)
                
            logger.info(f"YapÄ±landÄ±rma kaydedildi: {config_path}")
            return True
        
        except Exception as e:
            logger.error(f"YapÄ±landÄ±rma kaydetme hatasÄ±: {str(e)}")
            return False
    
